    private void loadData(){
        try{
            String acctno = jComboBox1.getSelectedItem().toString();
            int num = Integer.parseInt(acctno);
            c.sql = "select Name, Balance from indexreg" +
                    " join Balances on indexreg.Acctno = Balances.Acctno" +
                    "where indexreg.Acctno=?";
            c.sql = "select Acctno, Balance from Balances where Acctno =" +num;
            c.pst = c.connectDb().prepareStatement(c.sql);
            //c.pst.setString(1, acctno);
            c.rs = c.pst.executeQuery();
            if(c.rs.next()){
                jLabel7.setText(c.rs.getString(1));
                jTextField3.setText(c.rs.getString(2));
            }
            JOptionPane.showMessageDialog(null, "Account Found");
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try{
                c.rs.close();
                c.pst.close();
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
